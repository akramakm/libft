#include<libft.h>

int	ft_atoi(const char *str)
{
	int	i;
	int	k;
	int	s;

	i = 0;
	j = 0;
	s = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || \
		str[i] == '\v' || str[i] == '\r')
	{
		i++;
	}
	if (str[i] == '-')
	{
		s = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		k = (k * 10 + (str[i] - 48));
		i++;
	}
	return (k * s);
}
