#include<libft.h>

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = strlen (s) - 1;
	if (c == '\0')
		return ("");
	while (i >= 0)
	{
		if (s[i] == c)
		{
			return ((char *)s + i);
		}
		i--;
	}
	return (0);
}
